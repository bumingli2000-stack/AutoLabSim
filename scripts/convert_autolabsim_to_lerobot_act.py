#!/usr/bin/env python3
"""Batch-convert AutoLabSim NPZ episodes to LeRobotDataset v3 for ACT.

Tested against the LeRobot 0.4.4 public API.

Default source layout::

    data/episodes/screw_cap_batch/
      episode_000_seed_0000/
        episode.npz
        metadata.json
      episode_001_seed_0001/
        episode.npz
        metadata.json

Default output::

    data/lerobot/act/

The current AutoLabSim recorder stores the action together with the observation
obtained after executing that action. Therefore, the default alignment is:

    observation[t] -> action[t + 1]

Set ``--action-offset 0`` only after changing the recorder to save the
pre-action observation.
"""

from __future__ import annotations

import argparse
import gc
import importlib.metadata
import json
import shutil
import sys
import tempfile
import zipfile
from dataclasses import asdict, dataclass
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import numpy as np


DEFAULT_CAMERAS = (
    "image_overview_camera",
    "image_wrist_cam",
    "image_wrist_cam1",
)

TASK_TEXT_MAP = {
    "bimanual_unscrew_cap": (
        "Use both robot arms to hold the centrifuge tube and unscrew its cap."
    ),
}


@dataclass(frozen=True)
class ArrayHeader:
    shape: tuple[int, ...]
    dtype: str
    fortran_order: bool


@dataclass
class EpisodeRecord:
    source_dir: str
    npz_path: str
    metadata_path: str
    source_episode_index: int | None
    reset_seed: int | None
    source_task: str
    task_text: str
    success: bool | None
    raw_frames: int
    converted_frames: int


class ConversionError(RuntimeError):
    """Raised when source data are incompatible with the requested conversion."""


